#include "my_math.h"

// 四舍五入float_to_uint8
uint8_t float_to_uint8_round(float f)
{
	// 加0.5后转换为整数实现四舍五入
	uint32_t temp = (uint32_t)(f + 0.5f);

	// 确保值在0-255范围内
	if (temp > 255)
		return 255;

	return (uint8_t)temp;
}













